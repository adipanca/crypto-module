#include "cm_selftest.h"
#include "aes256.h"
#include <string.h>

cm_status_t kat_aes256(void) {
  const uint8_t key[32] = {
    0x60,0x3d,0xeb,0x10,0x15,0xca,0x71,0xbe,0x2b,0x73,0xae,0xf0,0x85,0x7d,0x77,0x81,
    0x1f,0x35,0x2c,0x07,0x3b,0x61,0x08,0xd7,0x2d,0x98,0x10,0xa3,0x09,0x14,0xdf,0xf4
  };
  const uint8_t iv[16] = {
    0x00,0x01,0x02,0x03,0x04,0x05,0x06,0x07,0x08,0x09,0x0a,0x0b,0x0c,0x0d,0x0e,0x0f
  };
  const uint8_t pt[16] = {
    0x6b,0xc1,0xbe,0xe2,0x2e,0x40,0x9f,0x96,0xe9,0x3d,0x7e,0x11,0x73,0x93,0x17,0x2a
  };
  const uint8_t exp[16] = {
    0xf5,0x8c,0x4c,0x04,0xd6,0xe5,0xf1,0xba,0x77,0x9e,0xab,0xfb,0x5f,0x7b,0xfb,0xd6
  };
  uint8_t ct[16];
  size_t ct_len = 0;
  if (aes256_cbc_encrypt(key, iv, pt, sizeof(pt), ct, &ct_len) != 0) return CM_ERR_SELFTEST;
  if (ct_len != 16) return CM_ERR_SELFTEST;
  return (memcmp(ct, exp, 16) == 0) ? CM_OK : CM_ERR_SELFTEST;
}
