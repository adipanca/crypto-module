#include "cm_selftest.h"
#include "hmac_sha256.h"
#include <string.h>

cm_status_t kat_hmac_sha256(void) {
  const uint8_t key[20] = {
    0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,
    0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b
  };
  const uint8_t msg[] = { 'H','i',' ','T','h','e','r','e' };
  const uint8_t exp[32] = {
    0xb0,0x34,0x4c,0x61,0xd8,0xdb,0x38,0x53,0x5c,0xa8,0xaf,0xce,0xaf,0x0b,0xf1,0x2b,
    0x88,0x1d,0xc2,0x00,0xc9,0x83,0x3d,0xa7,0x26,0xe9,0x37,0x6c,0x2e,0x32,0xcf,0xf7
  };
  uint8_t out[32];
  hmac_sha256_compute(key, sizeof(key), msg, sizeof(msg), out);
  return (memcmp(out, exp, 32) == 0) ? CM_OK : CM_ERR_SELFTEST;
}
