#include "cm_selftest.h"
#include "ctr_drbg.h"
#include <string.h>

/*
 * KAT CTR_DRBG (AES-256, no df) menggunakan vektor resmi NIST CAVP
 * (drbgvectors_no_reseed, [AES-256 no df], PredictionResistance=False),
 * COUNT 0:
 *   EntropyInput (48 byte) = df5d73fa...73bec010
 *   Nonce                  = (kosong)
 *   PersonalizationString  = (kosong)
 *   Pola CAVP: Instantiate -> Generate (dibuang) -> Generate (ReturnedBits)
 *   ReturnedBits (64 byte) = d1c07cd9...793e01c5
 *
 * Ini adalah KAT sejati: entropy input tetap, expected output tetap dari
 * vektor NIST, bukan sekadar pengecekan generate1 == generate2.
 */
cm_status_t kat_ctr_drbg(void) {
  static const uint8_t entropy[48] = {
    0xdf,0x5d,0x73,0xfa,0xa4,0x68,0x64,0x9e,0xdd,0xa3,0x3b,0x5c,0xca,0x79,0xb0,0xb0,
    0x56,0x00,0x41,0x9c,0xcb,0x7a,0x87,0x9d,0xdf,0xec,0x9d,0xb3,0x2e,0xe4,0x94,0xe5,
    0x53,0x1b,0x51,0xde,0x16,0xa3,0x0f,0x76,0x92,0x62,0x47,0x4c,0x73,0xbe,0xc0,0x10
  };
  static const uint8_t expected[64] = {
    0xd1,0xc0,0x7c,0xd9,0x5a,0xf8,0xa7,0xf1,0x10,0x12,0xc8,0x4c,0xe4,0x8b,0xb8,0xcb,
    0x87,0x18,0x9e,0x99,0xd4,0x0f,0xcc,0xb1,0x77,0x1c,0x61,0x9b,0xdf,0x82,0xab,0x22,
    0x80,0xb1,0xdc,0x2f,0x25,0x81,0xf3,0x91,0x64,0xf7,0xac,0x0c,0x51,0x04,0x94,0xb3,
    0xa4,0x3c,0x41,0xb7,0xdb,0x17,0x51,0x4c,0x87,0xb1,0x07,0xae,0x79,0x3e,0x01,0xc5
  };
  ctr_drbg_ctx_t ctx;
  uint8_t out[64];

  memset(&ctx, 0, sizeof(ctx));
  if (ctr_drbg_instantiate(&ctx, entropy, sizeof(entropy), NULL, 0, NULL, 0) != 0)
    return CM_ERR_SELFTEST;

  /* Generate pertama dibuang sesuai pola test CAVP. */
  if (ctr_drbg_generate(&ctx, out, sizeof(out)) != 0) return CM_ERR_SELFTEST;
  /* Generate kedua dibandingkan dengan ReturnedBits NIST. */
  if (ctr_drbg_generate(&ctx, out, sizeof(out)) != 0) return CM_ERR_SELFTEST;

  return (memcmp(out, expected, sizeof(expected)) == 0) ? CM_OK : CM_ERR_SELFTEST;
}
