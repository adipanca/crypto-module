#include "cm_selftest.h"
#include "ecdh_p256.h"
#include <string.h>

/*
 * KAT ECDH P-256 menggunakan vektor resmi NIST CAVP ECC CDH (P-256), COUNT 0.
 *   dIUT   (private key kita) = 7d7dc5f7...bc1a534
 *   QCAVS  (public key peer)  = (700c48f7..., db71e509...)
 *   ZIUT   (expected shared)  = 46fc6210...8997bd7b
 *
 * Ini adalah KAT sejati: private key tetap, public key peer tetap, dan
 * expected shared secret tetap, bukan sekadar pairwise consistency check.
 */
cm_status_t kat_ecdh_p256(void) {
  /* Private key d (32 byte) */
  static const uint8_t d_iut[32] = {
    0x7d,0x7d,0xc5,0xf7,0x1e,0xb2,0x9d,0xda,0xf8,0x0d,0x62,0x14,0x63,0x2e,0xea,0xe0,
    0x3d,0x90,0x58,0xaf,0x1f,0xb6,0xd2,0x2e,0xd8,0x0b,0xad,0xb6,0x2b,0xc1,0xa5,0x34
  };
  /* Public key peer Q = (x, y), 0x04 || x || y (uncompressed, 65 byte) */
  static const uint8_t peer_pub[65] = {
    0x04,
    0x70,0x0c,0x48,0xf7,0x7f,0x56,0x58,0x4c,0x5c,0xc6,0x32,0xca,0x65,0x64,0x0d,0xb9,
    0x1b,0x6b,0xac,0xce,0x3a,0x4d,0xf6,0xb4,0x2c,0xe7,0xcc,0x83,0x88,0x33,0xd2,0x87,
    0xdb,0x71,0xe5,0x09,0xe3,0xfd,0x9b,0x06,0x0d,0xdb,0x20,0xba,0x5c,0x51,0xdc,0xc5,
    0x94,0x8d,0x46,0xfb,0xf6,0x40,0xdf,0xe0,0x44,0x17,0x82,0xca,0xb8,0x5f,0xa4,0xac
  };
  /* Expected shared secret Z (32 byte) */
  static const uint8_t z_expected[32] = {
    0x46,0xfc,0x62,0x10,0x64,0x20,0xff,0x01,0x2e,0x54,0xa4,0x34,0xfb,0xdd,0x2d,0x25,
    0xcc,0xc5,0x85,0x20,0x60,0x56,0x1e,0x68,0x04,0x0d,0xd7,0x77,0x89,0x97,0xbd,0x7b
  };
  uint8_t z_actual[32];

  if (ecdh_p256_shared(d_iut, peer_pub, z_actual) != 0) return CM_ERR_SELFTEST;

  return (memcmp(z_actual, z_expected, 32) == 0) ? CM_OK : CM_ERR_SELFTEST;
}
