  print_hex("Result Output: ", hmac_result, sizeof(hmac_result));

  /* CTR-DRBG KAT dengan vektor resmi NIST CAVP (AES-256 no df) */
  memset(&drbg_ctx, 0, sizeof(drbg_ctx));
  ctr_drbg_instantiate(&drbg_ctx, drbg_entropy, sizeof(drbg_entropy), NULL, 0, NULL, 0);
  ctr_drbg_generate(&drbg_ctx, drbg_result, sizeof(drbg_result)); /* generate-1 dibuang */
